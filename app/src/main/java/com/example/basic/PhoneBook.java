package com.example.basic;

public class PhoneBook {
    private String name;
    private String tel;

    public void setName(String name) {
        this.name = name;
    }

    public void setTel(String number) {
        this.tel = number;
    }

    public String getTel() {
    return tel;
    }
    public String getname() {
        return name;
    }
}